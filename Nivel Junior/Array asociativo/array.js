 let pc1 = ["pc1" , "intel core i3" ,  "ram 8gb" , "500gb"] ;       

// ? pc2 es un array asociativo

 let pc2 = {                                                      
    nombre : "PC 2" ,
    procesador : "Intel core i7" ,
    ram : "16GB ram" ,
    espacio : "2TB"
 } ;   











// ! comentario descriptivo abajo


 
 
 // ? Un array asociativo, también conocido como objeto o diccionario, es una estructura de datos que permite almacenar pares de clave-valor. A diferencia de un array tradicional, donde los elementos se acceden mediante un índice numérico, en un array asociativo los elementos se acceden a través de claves o nombres descriptivos.

 // ? Cada propiedad se define mediante la sintaxis clave: valor. En este caso, las claves son nombres descriptivos y los valores son cadenas de texto que representan las características de un computador.

// * Para acceder a los valores almacenados en un objeto, se utiliza la notación de punto (objeto.propiedad) o la notación de corchetes (objeto["propiedad"]), donde "objeto" es el nombre de la variable que contiene el objeto y "propiedad" es el nombre de la propiedad a la que se desea acceder.

// * Por ejemplo, para obtener el valor de la propiedad nombre del objeto pc2, puedes usar pc2.nombre, lo cual te devolverá "PC 2". Del mismo modo, puedes acceder a las demás propiedades de pc2 de la siguiente manera:

/* 

pc2.procesador devuelve "Intel core i7"
pc2.ram devuelve "16GB ram"
pc2.espacio devuelve "2TB"

*/


// ? En resumen, un array asociativo u objeto en JavaScript es una estructura de datos que almacena pares de clave-valor. Se puede acceder a los valores utilizando la notación de punto o la notación de corchetes. pc2 es un objeto que representa las características de un computador.